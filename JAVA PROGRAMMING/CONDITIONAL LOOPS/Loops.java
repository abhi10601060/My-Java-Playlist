public class Loops {
    public static void main(String[] args) {

        for (int num = 0; num < 10; num+=3) {
            System.out.println(num);

        }
        for (int i=0 ; i<+100 ; i+=10){
            System.out.println(i);
        }

        for (int i = 10; i >=1 ; i--) {
            System.out.println(i);

        }

    }
}
